_METRICS_TOKEN = os.getenv("DREAM_METRICS_TOKEN", "").strip()


@app.get("/metrics")
async def metrics(request: Request):
    """Prometheus exposition. Requires Bearer token if DREAM_METRICS_TOKEN is set.

    Recommended deployment: set DREAM_METRICS_TOKEN to a long random secret
    and configure your Prometheus scrape config to send it as a Bearer token.
    Leaving the env unset disables auth (back-compat for single-replica dev).
    """
    if _METRICS_TOKEN:
        auth = request.headers.get("authorization", "")
        provided = auth.replace("Bearer ", "", 1).strip()
        # Constant-time comparison
        import hmac as _hmac
        if not provided or not _hmac.compare_digest(provided, _METRICS_TOKEN):
            from fastapi.responses import JSONResponse
            return JSONResponse(status_code=401, content={"detail": "Unauthorized"})

    # Refresh lazy gauges
    gauge("dreamapp_uptime_seconds", uptime_seconds())
    try:
        from models.user import UserRecord
        async with async_session() as db:
            r = await db.execute(select(func.count(UserRecord.id)))
            gauge("dreamapp_users_total", float(r.scalar() or 0))
    except Exception:
        pass
    # LLM cache utilization — visibility into cache hit potential
    try:
        from services.llm import llm_cache_stats
        stats = llm_cache_stats()
        gauge("dreamapp_llm_cache_size", float(stats.get("size", 0)))
    except Exception:
        pass
    return Response(content=render_metrics(), media_type="text/plain; version=0.0.4")


