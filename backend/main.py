@app.exception_handler(Exception)
async def _unhandled_exception_handler(request, exc):
    """Production-safe exception handler.

    - Always logs the full traceback (uvicorn swallows them by default).
    - In dev / when DREAM_DEBUG_ERRORS=1, returns the exception type+message
      to ease local debugging.
    - In production, returns a generic message — never leaks paths, SQL
      structure, or internal class names to the client.
    """
    import traceback
    import uuid as _uuid
    error_id = _uuid.uuid4().hex[:12]
    print(f"[ERROR {error_id}] {request.method} {request.url.path}")
    traceback.print_exc()
    from fastapi.responses import JSONResponse
    if _DEBUG_ERRORS:
        return JSONResponse(
            status_code=500,
            content={
                "detail": f"Internal Server Error: {type(exc).__name__}: {str(exc)[:200]}",
                "error_id": error_id,
            },
        )
    return JSONResponse(
        status_code=500,
        content={
            "detail": "Internal server error. Please try again or contact support.",
            "error_id": error_id,
        },
    )


