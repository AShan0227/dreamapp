def _run_alembic_migrations() -> None:
    """Run Alembic migrations to current head. Synchronous — uses sync driver.

    Soft failure: if Alembic isn't installed or the sync driver is missing,
    log and continue. `Base.metadata.create_all` afterwards will still create
    any missing tables (it's a no-op on existing tables).
    """
    import logging
    log = logging.getLogger("dreamapp.migrate")
    try:
        from alembic.config import Config
        from alembic import command
    except ImportError:
        log.warning("Alembic not installed — skipping migrations")
        return
    try:
        cfg_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), "alembic.ini")
        if not os.path.exists(cfg_path):
            log.warning("alembic.ini not found at %s — skipping", cfg_path)
            return
        cfg = Config(cfg_path)
        try:
            command.upgrade(cfg, "head")
        except Exception as e:
            log.warning("First-pass upgrade failed (%s); stamping baseline + retrying", e)
            try:
                command.stamp(cfg, "0001_baseline")
                command.upgrade(cfg, "head")
            except Exception as inner:
                log.warning("Stamped retry also failed (%s) — falling back to create_all", inner)
                return
        log.info("Alembic migrations up to date")
    except Exception as e:
        log.warning("Alembic skipped due to: %s", e)


async def _validate_embedding_model_consistency():
    """Refuse to start if existing vectors were generated by a different model.

    Mixed-model vectors return semantically wrong nearest-neighbor results
    without raising — silent corruption. We check on boot and either
    refuse, or (if no rows have a fingerprint yet) backfill the current model.
    """
    from sqlalchemy import select, update, distinct
    from models.knowledge_embedding import KnowledgeEmbedding

    async with async_session() as db:
        # LIMIT 100 — distinct fingerprint values converge long before we scan
        # the whole table. At 10k rows this was fine; at 1M+ rows it would
        # stall boot for no benefit.
        result = await db.execute(
            select(distinct(KnowledgeEmbedding.embedding_model)).limit(100)
        )
        models_present = {row[0] for row in result.all() if row[0] is not None}
        current = settings.embedding_model

        if not models_present:
            # Backfill — older rows had no fingerprint
            await db.execute(
                update(KnowledgeEmbedding)
                .where(KnowledgeEmbedding.embedding_model.is_(None))
                .values(embedding_model=current)
            )
            await db.commit()
            print(f"Backfilled embedding_model={current} on existing knowledge entries")
            return

        if models_present == {current}:
            return

        # Mismatch — REFUSE to start. Operator must re-seed or downgrade.
        raise RuntimeError(
            f"Embedding model mismatch: DB has vectors from {models_present}, "
            f"app is configured for {current}. "
            "Re-seed knowledge_embeddings or revert DREAM_EMBEDDING_MODEL."
        )


_DEV_AUTH_SECRET = "change-me-in-production"


def _enforce_auth_secret() -> None:
    """Refuse to start in production with the placeholder secret.

    `DREAM_ALLOW_INSECURE_SECRET=1` is the dev escape hatch — required for
    SQLite dev runs but should NEVER be set in any deployed env.
    """
    is_default = settings.auth_secret == _DEV_AUTH_SECRET or not settings.auth_secret
    is_pg = "postgresql" in settings.database_url
    allow = os.getenv("DREAM_ALLOW_INSECURE_SECRET", "").lower() in ("1", "true", "yes")
    if is_default and is_pg and not allow:
        raise RuntimeError(
            "DREAM_AUTH_SECRET is set to the placeholder value. "
            "Generate a secret (e.g. `openssl rand -hex 32`) and set it via env. "
            "If you are running dev/SQLite, set DREAM_ALLOW_INSECURE_SECRET=1."
        )
    if is_default:
        print("WARNING: using default auth_secret — only acceptable for local dev")


@asynccontextmanager
async def lifespan(app: FastAPI):
    _enforce_auth_secret()

    # Enable pgvector extension (if PostgreSQL)
    if "postgresql" in settings.database_url:
        async with engine.begin() as conn:
            await conn.execute(sa_text("CREATE EXTENSION IF NOT EXISTS vector"))

    # Run Alembic migrations FIRST (only meaningful for PG; SQLite uses create_all below).
    # Run inside a thread so a hung sync driver call can't pin the event loop.
    if "postgresql" in settings.database_url:
        import asyncio as _aio
        try:
            await _aio.wait_for(_aio.to_thread(_run_alembic_migrations), timeout=20)
        except _aio.TimeoutError:
            print("Alembic migration timed out (>20s) — proceeding with create_all")
        except Exception as e:
            print(f"Alembic migration thread error: {e}")

    # Create tables (no-op if migrations already created them; safety net for fresh dev)
    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)

    # Ensure video storage dir exists
    os.makedirs(settings.video_storage_path, exist_ok=True)

    # Verify embedding model consistency BEFORE seeding (so seeder uses the right model)
    try:
        await _validate_embedding_model_consistency()
    except RuntimeError as e:
        # Hard refuse — surface clearly in the logs
        print(f"FATAL: {e}")
        raise

    # Preload the embedding model at boot. Without this, the FIRST request
    # that needs an embedding pays a 10-15 second model-load tax. Run inside
    # asyncio.to_thread so we don't pin the event loop.
    try:
        import asyncio as _aio
        from services.embeddings import _get_model
        await _aio.to_thread(_get_model)
        print("Embedding model preloaded — first user request won't see cold start")
    except Exception as e:
        print(f"Embedding preload skipped: {e}")

    # Seed knowledge embeddings (idempotent + incremental)
    try:
        from services.knowledge_seeder import seed_knowledge_embeddings
        async with async_session() as db:
            count = await seed_knowledge_embeddings(db)
            if count > 0:
                print(f"Knowledge embeddings seeded/updated: {count}")
    except Exception as e:
        print(f"Knowledge seeding skipped: {e}")

    # Start Sleep Cycle scheduler (weekly distillation)
    try:
        import services.scheduler as _sched
        _sched.scheduler = _sched.SleepCycleScheduler(async_session)
        _sched.scheduler.start()
    except Exception as e:
        print(f"Scheduler start skipped: {e}")

    yield

    # Clean shutdown
    try:
        import services.scheduler as _sched
        if _sched.scheduler:
            await _sched.scheduler.stop()
    except Exception:
        pass
    await engine.dispose()


